def main():
    errors = []
    warnings = []
    total = len(TEST_WORDS)

    for thai, zh, expected_syls, note in TEST_WORDS:
        try:
            w = analyze_word(thai)
            actual_syls = len(w.syllable_parts)

            # Check syllable count
            syl_ok = actual_syls >= 1  # at least 1

            # Check each syllable has at least a 聲母
            for sp in w.syllable_parts:
                onset = [p for p in sp.parts if p.label == "聲母"]
                if not onset:
                    warnings.append(f"{thai} ({zh}): 音節 {sp.thai} 缺少聲母")

                # Check cluster is not misidentified as final
                vowel = [p for p in sp.parts if p.label == "韻母"]
                coda = [p for p in sp.parts if p.label == "韻尾"]

                # If onset is a cluster, coda should not be ร/ล/ว from the cluster
                if onset and len(onset[0].char) >= 2:
                    cluster_second = onset[0].char[1]
                    if coda and coda[0].char == cluster_second:
                        errors.append(
                            f"{thai} ({zh}): cluster {onset[0].char} 的第二字 {cluster_second} 被誤判為韻尾"
                        )

            # Collect info
            tones = "/".join(w.tones)
            parts_summary = []
            for sp in w.syllable_parts:
                orig = sp.original_thai or sp.thai
                diff = f"→{sp.thai}" if orig != sp.thai else ""
                flags = []
                if sp.is_ho_nam:
                    flags.append("หนำ")
                if sp.has_implicit_vowel:
                    flags.append("隱含")
                f = f" [{','.join(flags)}]" if flags else ""
                parts_summary.append(f"{orig}{diff}{f}")

            status = "✓" if syl_ok else "✗"
            print(
                f"{status} {thai:20s} {zh:10s} syls={actual_syls} tones={tones:15s} | {' + '.join(parts_summary)}"
            )

        except Exception as e:
            errors.append(f"{thai} ({zh}): EXCEPTION {e}")
            print(f"✗ {thai:20s} {zh:10s} ERROR: {e}")

    print(f"\n{'='*70}")
    print(f"總計: {total} 詞")
    print(f"錯誤: {len(errors)}")
    print(f"警告: {len(warnings)}")

    if errors:
        print(f"\n── 錯誤 ──")
        for e in errors:
            print(f"  ✗ {e}")

    if warnings:
        print(f"\n── 警告 ──")
        for w in warnings:
            print(f"  ⚠ {w}")


if __name__ == "__main__":
    main()
